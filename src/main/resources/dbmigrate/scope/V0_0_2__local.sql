CREATE TABLE SCOPE_LOCAL(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
        NAME VARCHAR(50),
	GLOBAL_ID BIGINT,
	SHARED INTEGER,
	REFERENCE VARCHAR(50),
        CONSTRAINT PK_SCOPE_LOCAL PRIMARY KEY(ID),
	CONSTRAINT FK_SCOPE_LOCAL_GLOBAL FOREIGN KEY(GLOBAL_ID) REFERENCES SCOPE_GLOBAL(ID)
);

