

-------------------------------------------------------------------------------
--  group base
-------------------------------------------------------------------------------
CREATE TABLE GROUP_BASE(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
        CODE VARCHAR(50),
	ABBR VARCHAR(50),
        NAME VARCHAR(200),
	DESCN VARCHAR(200),
        STATUS INTEGER,
	GLOBAL_ID BIGINT,
	LOCAL_ID BIGINT,
	GROUP_TYPE_ID BIGINT,
	REFERENCE VARCHAR(200),
        CONSTRAINT PK_GROUP_BASE PRIMARY KEY(ID),
        CONSTRAINT FK_GROUP_TYPE FOREIGN KEY(GROUP_TYPE_ID) REFERENCES GROUP_TYPE(ID)
);

