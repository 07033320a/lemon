CREATE TABLE AUTH_ROLE_DEF(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
        NAME VARCHAR(50),
	GLOBAL_ID BIGINT,
	LOCAL_ID BIGINT,
        DESCN VARCHAR(200),
        CONSTRAINT PK_AUTH_ROLE_DEF PRIMARY KEY(ID)
);
