CREATE TABLE AUTH_OPER(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
        NAME VARCHAR(50),
	MASK INTEGER,
	CODE CHAR(1),
        DESCN VARCHAR(200),
	GLOBAL_ID BIGINT,
	LOCAL_ID BIGINT,
        CONSTRAINT PK_AUTH_OPER PRIMARY KEY(ID)
);
