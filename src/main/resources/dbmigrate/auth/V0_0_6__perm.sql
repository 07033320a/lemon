CREATE TABLE AUTH_PERM(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
	CODE VARCHAR(200),
	NAME VARCHAR(200),
	RESC_ID BIGINT NOT NULL,
	OPER_ID BIGINT,
	SCOPE_ID VARCHAR(50),
        CONSTRAINT PK_AUTH_PERM PRIMARY KEY(ID),
	CONSTRAINT FK_AUTH_PERM_RESC FOREIGN KEY(RESC_ID) REFERENCES AUTH_RESC(ID),
	CONSTRAINT FK_AUTH_PERM_OPER FOREIGN KEY(OPER_ID) REFERENCES AUTH_OPER(ID)
);
