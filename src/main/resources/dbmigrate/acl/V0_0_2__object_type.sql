CREATE TABLE ACL_OBJECT_TYPE(
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
	CODE VARCHAR(50),
        NAME VARCHAR(200),
	GLOBAL_ID BIGINT,
	LOCAL_ID BIGINT,
        CONSTRAINT PK_ACL_OBJECT_TYPE PRIMARY KEY(ID)
);

